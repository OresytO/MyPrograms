DROP TABLE IF EXISTS "VISIT";
DROP TABLE IF EXISTS "VISITOR";
CREATE TABLE "VISITOR" (
  ID INTEGER PRIMARY KEY,
  NAME VARCHAR(255) NOT NULL,
  CREATE_DATE DATE NOT NULL DEFAULT CURRENT_TIMESTAMP
);
