CREATE TABLE "VISIT" (
  ID INTEGER PRIMARY KEY,
  VISITOR_ID INTEGER REFERENCES "VISITOR"(id),
  DATE_OF_VISIT DATE NOT NULL DEFAULT CURRENT_TIMESTAMP
);
