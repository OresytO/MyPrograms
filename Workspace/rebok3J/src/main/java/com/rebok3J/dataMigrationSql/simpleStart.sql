CREATE TABLE "USER" (
  ID            INTEGER PRIMARY KEY,
  USER_NAME     VARCHAR(255)        NOT NULL,
  USER_NICKNAME VARCHAR(255) UNIQUE NOT NULL,
  PASSWORD      VARCHAR(255)        NOT NULL,
  ENABLE        BOOLEAN             NOT NULL
);
